package com.hotel.po;

public class RoomBookCustom{
	private String type;
	private String date;
	private String username;
	private String num;
	private int rb_id;
	private String u_username;
	private String u_name;
	private String r_num;
	private String rb_bookdate;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public int getRb_id() {
		return rb_id;
	}
	public void setRb_id(int rb_id) {
		this.rb_id = rb_id;
	}
	public String getU_username() {
		return u_username;
	}
	public void setU_username(String u_username) {
		this.u_username = u_username;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String u_name) {
		this.u_name = u_name;
	}
	public String getR_num() {
		return r_num;
	}
	public void setR_num(String r_num) {
		this.r_num = r_num;
	}
	public String getRb_bookdate() {
		return rb_bookdate;
	}
	public void setRb_bookdate(String rb_bookdate) {
		this.rb_bookdate = rb_bookdate;
	}
	
}
